@Entity
@Inheritance(strategy = InheritanceType.JOINED)
public class Observacao extends PersistentObjectAudit {

	/** Atributo serialVersionUID. */
	private static final long serialVersionUID = 6740031314677082190L;

	/** Atributo id. */
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@JsonView({ Views.GenericView.class })
	private Long id;

	/** Atributo descricao. */
	@Column(length = 500)
	@JsonView({ Views.MapaOrganizacionalListView.class, Views.PessoaEditView.class, Views.GenericView.class, Views.ComissaoEditView.class })
	private String descricao;

	/**
	 * Retorna o valor do atributo <code>id</code>
	 *
	 * @return <code>Long</code>
	 */
	public Long getId() {

		return id;
	}

	/**
	 * Define o valor do atributo <code>id</code>.
	 *
	 * @param id
	 */
	public void setId(Long id) {

		this.id = id;
	}

	/**
	 * Retorna o valor do atributo <code>descricao</code>
	 *
	 * @return <code>String</code>
	 */
	public String getDescricao() {

		return descricao;
	}

	/**
	 * Define o valor do atributo <code>descricao</code>.
	 *
	 * @param descricao
	 */
	public void setDescricao(String descricao) {

		this.descricao = descricao;
	}

	@Override
	@JsonView({ Views.GenericView.class })
	public Calendar getDataCriacao() {

		return super.getDataCriacao();
	}

}
